import "antd/dist/antd.css";

import { Layout, PageHeader } from "antd";
import { ListOrdemResponse, OrdemResponse } from "../services/types";
import { useEffect, useState } from "react";

import AnimatedContainer from "../components/AnimatedContainer";
import Catalog from "../components/Catalog";
import FooterComponent from "../components/Footer";
import { GetServerSideProps } from "next";
import Head from "next/head";
import HeaderComponent from "../components/Header";
import Image from "next/image";
import SiderComponent from "../components/Sider";
import { getOrderList } from "../services/ordem";
import styles from "../../styles/Home.module.css";

const { Content, Footer } = Layout;

interface Props {
  itemsList: ListOrdemResponse;
  searchResults: ListOrdemResponse;
}

export const Home = ({ itemsList, searchResults }: Props) => {
  const [items, setItems] = useState<OrdemResponse[]>([]);

  useEffect(() => {
    if (searchResults) {
      setItems(searchResults.itens);
    } else {
      setItems(itemsList.itens);
    }
  }, [searchResults]);

  return (
    <AnimatedContainer>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Layout>
        <HeaderComponent />
        <Layout className={styles.main} hasSider>
          <SiderComponent />
          <Layout>
            <Content>
              <Catalog items={items} />
            </Content>
          </Layout>
        </Layout>
      </Layout>
      <Layout className={styles.footer}>
        <FooterComponent />
      </Layout>
    </AnimatedContainer>
  );
};

export const getServerSideProps: GetServerSideProps<Props> = async ({
  query,
}) => {
  // ...
  const data = await getOrderList();
  let searchResults: ListOrdemResponse = null;
  if (query) {
    const { search } = query;
    searchResults = await getOrderList({ pesquisa: search });
  }

  return {
    props: {
      itemsList: data,
      searchResults,
    },
  };
};

export default Home;
